package com.swcguild.commliving.model;

/**
 *
 * @author apprentice
 */
public class UserCost {
    private String username;
    private int rentersInsurance;
    private int utilities;
    private int baseRent;
    private int internet;
    private int water;
    private int trash;
    private int electric;
    private int gas;

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public int getRentersInsurance() {
        return rentersInsurance;
    }

    public void setRentersInsurance(int rentersInsurance) {
        this.rentersInsurance = rentersInsurance;
    }

    public int getUtilities() {
        return utilities;
    }

    public void setUtilities(int utilities) {
        this.utilities = utilities;
    }

    public int getBaseRent() {
        return baseRent;
    }

    public void setBaseRent(int baseRent) {
        this.baseRent = baseRent;
    }

    public int getInternet() {
        return internet;
    }

    public void setInternet(int internet) {
        this.internet = internet;
    }

    public int getWater() {
        return water;
    }

    public void setWater(int water) {
        this.water = water;
    }

    public int getTrash() {
        return trash;
    }

    public void setTrash(int trash) {
        this.trash = trash;
    }

    public int getElectric() {
        return electric;
    }

    public void setElectric(int electric) {
        this.electric = electric;
    }

    public int getGas() {
        return gas;
    }

    public void setGas(int gas) {
        this.gas = gas;
    }
    
    
    
}
